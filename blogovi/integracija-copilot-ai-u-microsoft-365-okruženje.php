	<p class="content mb-3 mt-3" style="padding-top:25px;">
	
	Dolazak umjetne inteligencije Copilot u ekosistem Microsoft 365 predstavlja značajan korak naprijed u evoluciji radnog okruženja svakog poslovnog korisnika. Ova inovativna tehnologija nije samo alat, već digitalni partner koji obećava povećanje efikasnosti, produktivnosti i kreativnosti korisnika. U ovom članku istražujemo ključne novosti vezane uz integraciju Copilot AI u Microsoft 365 i sve njegove aplikacije, te kako korisnici mogu pristupiti i implementirati ovu tehnologiju u svoje dnevne poslovne aktivnosti.<br><br>
<h2>Copilot se pridružuje 365 okružju</h2><br>
Počevši s 1. studenim 2023. godine Copilot AI je dostupan na tržištu za sve Enterprise level licence (najčešće poznate kao E3 i E5 licence) te je dizajniran da djeluje kao asistent unutar Microsoft 365 aplikacija, uključujući Outlook, Word, Excel, PowerPoint i Teams (za početak). Osim navedenih aplikacija, Copilot ima pristup kompletnom Microsoft Graphu (centralni Microsoft API koji ima pristup podacima i inteligenciji unutar Microsoft 365 ekosistema).<br><br>Ova tehnologija koristi napredne algoritme za razumijevanje konteksta i potreba korisnika, što omogućuje automatsko generiranje e-mailova, izvještaja, analiza podataka, te čak i kreiranje prezentacija.<br><br>

<h2>Što uključuju najznačajnije novosti?</h2><br>
<b>Contextual Understanding:</b> Copilot AI može analizirati prethodne interakcije i dokumente kako bi predvidio potrebe korisnika i pružio relevantne prijedloge i sadržaj.<br>
<b>Real-time Collaboration:</b> Omogućava timovima da u stvarnom vremenu surađuju koristeći AI kao medijatora, što poboljšava komunikaciju i smanjuje potrebu za stalnim sastancima.<br>
<b>Learning Capabilities:</b> Ova tehnologija uči iz svakodnevne upotrebe, što omogućava personalizaciju iskustva i povećanje produktivnosti.<br><br>
<h2>Kako dobiti licencu za Copilot AI?</h2><br>
Za pristup Copilot AI, korisnici moraju imati aktivnu licencu za Microsoft 365, s tim da Copilot AI zahtijeva dodatnu nadoplatu ili specifičnu vrstu licence. Trenutno, Microsoft nudi Copilot AI kao dio paketa za enterprise korisnike, ali planira proširenje na druge pakete ovisno o potražnji i feedbacku korisnika.<br><br>

Zainteresirani korisnici mogu se obratiti Microsoft prodajnim timovima ili provjeriti na službenim Microsoft web stranicama kako bi dobili više informacija o licencama i cijenama. Također, postoji mogućnost probnog razdoblja koje omogućava korisnicima da testiraju funkcionalnosti Copilot AI prije konačne odluke o kupovini.	
